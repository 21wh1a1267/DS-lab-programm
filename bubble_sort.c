#include<stdio.h>
int main()
{
int arr[50],n,i,d,swap;
printf("enter the no of elements");
scanf("%d",&n);
printf("enter %d integer\n",n);
for(i=0;i<n;i++)
{
scanf("%d",&arr[i]);
}
{
for(i=0;i<n-1;i++);
{
for(d=0;d<n;i++);
{
	if(arr[d]>arr[d+1])
	{
		swap=arr[d];
		arr[d]=arr[d+1];
		arr[d+1]=swap;
	}
}
}
}
printf("sorted list in ascending");
for(i=0;i<n;i++)
{
printf("\n%d",arr[i]);
}
return 0;
}




	
